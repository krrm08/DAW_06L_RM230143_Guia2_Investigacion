let frutas = [];

// Utilizamos push() para agregar elementos al final del array
frutas.push("Manzana");
frutas.push("Banana");
frutas.push("Naranja");
frutas.push("Fresa")

console.log("Arreglo después de push:", frutas);

let removedfrutas = frutas.pop();

console.log("Array después de pop:", frutas);

console.log("Se ha borrado el elemento:", removedfrutas);

